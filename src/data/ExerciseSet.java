package data;

public class ExerciseSet {
    private int reps;
    private double kilos;

    public ExerciseSet(int reps, double kilos) {
        this.reps = reps;
        this.kilos = kilos;
    }

    public int getReps() {
        return reps;
    }

    public double getKilos() {
        return kilos;
    }
}
